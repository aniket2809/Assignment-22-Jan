public class Student {
    private int roll;
    private String name;
    private int marks;

    // Adding Getter and Setter
    public int getRoll(){
        return this.roll;
    }
    public void setRoll(int roll){
        this.roll=roll;
    }

    public String getName(){
        return this.name;
    }
    public void setName(String name){
        this.name=name;
    }
    public int getMarks(){
        return this.marks;
    }
    public void setMarks(int marks){
        this.marks=marks;
    }

}
